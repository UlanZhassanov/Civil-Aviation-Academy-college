	 * @return \Illuminate\Http\Response
	 */
	public function index()
	{
		// Filter
		$whereArray = [
			'status' => '1'
		];
		$data = Applications::select('*')
			->where($whereArray)
			->orderBy('updated_at', 'desc')
			->paginate(100)
			->appends($whereArray);
		// Count
		$countData = Applications::select('*')
			->where($whereArray)
			->count();
		// Data
		$dataArr = [
			'data' => $data,
			'countData' => $countData
		];
		return view('admin.enrollee.deleted', $dataArr);
	}

